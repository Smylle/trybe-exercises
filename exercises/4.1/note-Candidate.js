let estado = 'Lista';

switch (estado) {
    case 'Aprovado':
        console.log('Parabéns, APROVADO!');
        break;

    case 'Lista':
        console.log('Você ingressou em nossa LISTA!');
        break;

    case 'Reprovado':
        console.log('Parabéns, REPROVADO!');
        break;

    default:
        console.log('Não se aplica.');

}